//This program will demonstrate the use of escape sequences
//by displaying a formatted verse in the console window

public class ShowEscapeSequence
{
	public static void main(String[] args)
	{
		//display string output using newline escape sequence
		System.out.print("Computers, computers everywhere\n  as far as I can see");
		System.out.println("\nI really, really like these things,\n  Oh joy, oh joy for me!");
	}
}