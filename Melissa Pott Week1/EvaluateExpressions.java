//This program is an example of evaluating various mathematical expressions in Java


public class EvaluateExpressions
{
	public static void main(String[] args)
	{
		//build each output string consisting of a label and result of calculcation
		System.out.println("3.0 * 5.0 = " + (3.0*5.0));
		System.out.println("7.1 * 8.3 - 2.2 = " + (7.1*8.3-2.2));
		System.out.println("3.2 / (6.1 * 5) = " + (3.2/(6.1*5)));
	}
}